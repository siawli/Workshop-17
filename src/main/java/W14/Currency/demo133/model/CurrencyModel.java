package W14.Currency.demo133.model;

public class CurrencyModel {
    private String currencyID;
    private String currencyAndSymbol;
    private String symbol;
    private String currencyName;

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getCurrencyName() {
        return currencyName;
    }

    public void setCurrencyName(String currencyName) {
        this.currencyName = currencyName;
    }

    public String getCurrencyAndSymbol() {
        return currencyAndSymbol;
    }

    public void setCurrencyAndSymbol(String currencyAndSymbol) {
        this.currencyAndSymbol = currencyAndSymbol;
    }

    public String getCurrencyID() {
        return currencyID;
    }

    public void setCurrencyID(String currencyID) {
        this.currencyID = currencyID;
    }

    


}
